"""Настраиваемые константы системы скоринга (политика, без I/O).

Значения и обоснование — в спецификации скоринга (§7 «Настраиваемые
константы»). Все они зафиксированы на сезон и публикуются в правилах заранее;
рекалибровка ``GRADATIONS`` — только между сезонами (см. ``recalibration``).

Числа здесь — обычные ``float``: статистика скоринга (``log2``, ``sqrt``,
усадка) по своей природе вещественная. На границе с БД результат квантуется в
``Decimal`` нужной точности (``numeric(6,5)``) в адаптерах.
"""

from __future__ import annotations

import math

# Крайние точки шкалы вероятностей (минимальная/максимальная градации).
P_MIN: float = 0.10
P_MAX: float = 0.90

# Дефолтная сетка градаций «уверенность → вероятность» (5 точек). Совпадает с
# ``_GRADE_PROBABILITY`` домена predictions и ``LeagueConfig.default()`` домена
# seasons (единый нейтральный маппинг; связка зафиксирована тестом
# ``test_default_config_matches_scoring``). Прогнозы снапшотятся по этой сетке;
# скоринг переносит их на замороженную сетку сезона по позиции градации.
DEFAULT_GRADATIONS: tuple[float, ...] = (0.10, 0.30, 0.50, 0.70, 0.90)

# Нормировка разброса мнений: максимальный возможный ``σ`` при крайних
# градациях (половина голосов 0.1, половина 0.9) = (P_MAX − P_MIN) / 2.
DISP_NORM: float = (P_MAX - P_MIN) / 2  # = 0.40

# Нормировка неожиданности: максимум ``−log2(p)`` на крайней точке шкалы.
SURPRISE_NORM: float = -math.log2(P_MIN)  # = 3.3219… бит

# Доля разногласия в весе события (остальное — неожиданность исхода).
BETA: float = 0.35

# Константа усадки сезонного рейтинга (во «взвешенных событиях»).
K_SHRINK: float = 6.0

# Тайм-вейтинг: надбавка к весу прогноза за «ранность». Прогноз в момент
# открытия приёма (максимум неопределённости, меньше всего информации) весит
# в ``1 + TIME_WEIGHT_LAMBDA`` раз больше прогноза у самого закрытия. Мягкий
# наклон: угадать рано и против толпы ценнее, но не доминирует над сложностью.
TIME_WEIGHT_LAMBDA: float = 0.5

# Событие участвует в рейтинге, только если предсказателей не меньше этого.
MIN_PREDICTORS: int = 5

# Пороги квалификации к призам: объём / разнообразие категорий / охват сложности.
N_MIN: int = 30
C_MIN: int = 4
W_MIN: float = 8.0

# z-значение доверительного интервала калибровки (95%).
Z: float = 1.96
